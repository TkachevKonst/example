void PrintArray(int[,] matrix)
{
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            Console.Write($" {matrix[i, j]}");
        }
        Console.WriteLine();
    }
}
void PrintClear(int[,] matrix)
{
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            if (matrix[i, j] == 0) Console.Write($" ");
            else Console.Write($"*");
        }
        Console.WriteLine();
    }
}
void FullPic(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FullPic(row - 1, col);
        FullPic(row, col - 1);
        FullPic(row + 1, col);
        FullPic(row, col + 1);
    }
}